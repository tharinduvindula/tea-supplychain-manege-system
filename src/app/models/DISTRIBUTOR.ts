export class DISTRIBUTOR {
    contactNumber = null;
    email = null;
    emailCode = null;
    name = null;
    userAccess = null;
    userAddress = null;

    constructor(contactNumber, email, emailCode, name, userAccess, userAddress) {
        this.contactNumber = contactNumber;
        this.email = email;
        this.emailCode = emailCode;
        this.name = name;
        this.userAccess = userAccess;
        this.userAddress = userAddress;
    }
}
